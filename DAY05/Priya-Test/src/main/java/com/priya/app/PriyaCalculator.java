package com.priya.app;

public class PriyaCalculator {

    public int add(int fir, int sec) {
        return fir+sec;
    }

    public int subtract(int first, int second) {
        return first-second;
    }

    public int Multi(int a, int b) {
        return a*b;
    }

    public int divide(int x, int y) {
        return x/y;
    }

}
